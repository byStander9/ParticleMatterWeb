public class PlaceVo {

	private String place_name;
	private String place_link;
	private String place_roadAddress;
	private String place_img;
	public String getPlace_name() {
		return place_name;
	}
	public void setPlace_name(String place_name) {
		this.place_name = place_name;
	}
	public String getPlace_link() {
		return place_link;
	}
	public void setPlace_link(String place_link) {
		this.place_link = place_link;
	}
	public String getPlace_roadAddress() {
		return place_roadAddress;
	}
	public void setPlace_roadAddress(String place_roadAddress) {
		this.place_roadAddress = place_roadAddress;
	}
	public String getPlace_img() {
		return place_img;
	}
	public void setPlace_img(String place_img) {
		this.place_img = place_img;
	}
	
	
}
